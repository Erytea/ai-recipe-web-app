"""
Скрипт для импорта продуктов из скопированных данных calorizator.ru
"""

import re


def parse_calorizator_data(text: str) -> dict:
    """Парсит данные, скопированные с calorizator.ru"""
    
    products = {}
    lines = text.strip().split('\n')
    
    for line in lines:
        # Пропускаем заголовки и пустые строки
        if not line.strip() or 'Продукт' in line or 'Бел, г' in line:
            continue
        
        # Разбиваем по табуляции
        parts = line.split('\t')
        
        # Ищем паттерн: Название Название Белки Жиры Углеводы Калории
        # или: Название Белки Жиры Углеводы Калории
        
        # Пытаемся найти числа в конце строки
        numbers = []
        name_parts = []
        
        for part in parts:
            part = part.strip()
            if not part:
                continue
            
            # Пытаемся распарсить как число
            try:
                num = float(part.replace(',', '.'))
                numbers.append(num)
            except ValueError:
                # Это часть названия
                if len(numbers) == 0:  # Числа еще не начались
                    name_parts.append(part)
        
        # Должно быть ровно 4 числа: белки, жиры, углеводы, калории
        if len(numbers) == 4 and len(name_parts) > 0:
            # Берем последнюю часть названия как основное имя (обычно дубликат)
            name = name_parts[-1] if len(name_parts) > 1 else name_parts[0]
            name = name.lower().strip()
            
            # Иногда название дублируется, берем более короткое
            if len(name_parts) > 1:
                # Если первое и второе одинаковые, берем одно
                if name_parts[0] == name_parts[1]:
                    name = name_parts[0].lower().strip()
            
            protein, fat, carbs, calories = numbers
            
            products[name] = {
                "calories": calories,
                "protein": protein,
                "fat": fat,
                "carbs": carbs,
                "category": "mushrooms"
            }
    
    return products


